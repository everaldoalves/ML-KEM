from Crypto.Hash import SHAKE128

# O vetor como um array de bytes
data = bytes([
    0xa5, 0x4b, 0x2a, 0x76, 0xb9, 0x81, 0x2c, 0xc1,
    0x45, 0x2c, 0x3d, 0xa4, 0xe1, 0x3b, 0x8f, 0xab,
    0x23, 0x45, 0xf6, 0xa7, 0xb8, 0xc9, 0xd0, 0x1e,
    0x2f, 0x30, 0x31, 0x12, 0x33, 0x44, 0x55, 0x66,
    0x01, 0x02
])

# Crie um objeto SHAKE128
shake = SHAKE128.new(data)

# Especifique quantos bytes de saída você deseja; neste caso, vamos assumir 512 bytes
output_bytes = shake.read(64)

# Imprime o resultado em hexadecimal
print(output_bytes.hex())

